/// One gauge series' identity: `mem_usage_bytes{service,instance,slot}`.
/// `stale_from`, when set, is the sample index at which this series
/// **stops emitting** (issue #33 architect plan: "a deterministic subset
/// stops emitting partway through the window (staleness gaps)") â€” OTLP
/// carries no StaleNaN marker, so this is the only representation the
/// collector-fed corpus can carry (the direct-remote-write StaleNaN
/// micro-case in `metrics.rs` supplements this, per the task-manager
/// resolution).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct GaugeSeries {
    pub service_idx: usize,
    pub instance_idx: usize,
    pub slot_idx: usize,
    pub stale_from: Option<usize>,
}

/// One counter series' identity: `requests_total{service,instance}`.
/// `reset_at`, when set, is the sample index at which the cumulative value
/// **drops to zero** (architect plan: "a deterministic subset injects
/// counter resets (value drops to 0 at pinned timestamps)") before
/// resuming accumulation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct CounterSeries {
    pub service_idx: usize,
    pub instance_idx: usize,
    pub reset_at: Option<usize>,
}

/// One histogram data-point identity: `request_duration_seconds{service,
/// instance}` â€” flattens (collector-side, not here â€” see module doc
/// comment) to `_bucket{le}` Ã— `histogram_bounds.len() + 1` (incl. `+Inf`)
/// plus `_sum`/`_count`, "â‰ˆ10 series per instance" per the architect plan.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct HistogramSeries {
    pub service_idx: usize,
    pub instance_idx: usize,
}

/// One flattened (post-collector-conversion) series' bookkeeping entry â€”
/// only what the completeness manifest ([`Corpus::expected_series`]/
/// [`Corpus::expected_samples`]) needs: a name (diagnostic only) and how
/// many of this corpus's timestamps it actually carries a real sample at
/// (`sample_count` for counters/histograms, which never gap; `stale_from`
/// for gauges, which may).
#[derive(Debug, Clone)]
pub struct SeriesSpec {
    pub metric_name: String,
    pub samples_emitted: usize,
}

/// One concrete, post-flatten series' full label identity plus the set of
/// activity-bucket floors (`ACTIVITY_BUCKET_MS`) it has **at least one
/// real sample in** â€” the basis for the historical-discovery
/// differential's expectation (issue #33 architect adjudication, finding
/// 2, `metrics.rs`'s `assert_discovery_contract_window`). This is
/// **our own documented bucket-floor contract's** answer (docs/schemas.md
/// Â§2.1: a query window is bucket-floored on both bounds, and any series
/// with a `metric_series` row â€” one per activity bucket it was ever
/// active in â€” whose bucket falls inside `[floor(start), floor(end)]` is
/// returned, a bounded superset of the exact-sample-window set) â€” **not**
/// Prometheus's exact-sample-window answer, which the differential now
/// treats as a logged reference, never the expectation, for this
/// specific class of endpoint (`/series`/`/labels`/label-values only â€”
/// the query **value matrix** stays a strict, never-allowlisted
/// Prometheus-equality comparison, untouched by this).
#[derive(Debug, Clone)]
pub struct DiscoverableSeries {
    pub labels: Vec<(String, String)>,
    pub buckets: BTreeSet<i64>,
}

/// A generated corpus: every family's series identities (enough to
/// rebuild every sample value via the pure `*_value`/`*_bucket_counts` fns
/// below), the flattened manifest ([`expected_series`]/[`expected_samples`]
/// â€” issue #33 amendment's completeness pre-check), and the window it
/// spans.
#[derive(Debug, Clone)]
pub struct Corpus {
    pub run_id: String,
    pub scale: Scale,
    pub seed: u64,
    pub base_ms: i64,
    pub step_ms: i64,
    pub sample_count: usize,
    pub gauges: Vec<GaugeSeries>,
    pub counters: Vec<CounterSeries>,
    pub histograms: Vec<HistogramSeries>,
    pub histogram_bounds: Vec<f64>,
    pub series: Vec<SeriesSpec>,
    pub expected_series: usize,
    pub expected_samples: usize,
    pub first_ts_ms: i64,
    pub last_ts_ms: i64,
}

impl Corpus {
    /// The completeness pre-check's manifest (issue #33 code review,
    /// [medium]): metric name -> exact `(expected_series,
    /// expected_samples)`, derived from [`Self::series`] â€” **per metric
    /// name**, never collapsed to a grand total (a compensating miscount
    /// across metric families, e.g. `mem_usage_bytes` short 5,
    /// `requests_total` over by 5, would otherwise net to the same total
    /// and false-pass a sum-only comparison â€” see `metrics.rs`'s
    /// `Manifest` type).
    pub fn per_metric_manifest(&self) -> BTreeMap<String, (usize, usize)> {
        let mut manifest: BTreeMap<String, (usize, usize)> = BTreeMap::new();
        for s in &self.series {
            let entry = manifest.entry(s.metric_name.clone()).or_insert((0, 0));
            entry.0 += 1;
            entry.1 += s.samples_emitted;
        }
        manifest
    }

    /// Every concrete, post-flatten series this corpus produces, with its
    /// exact label identity and activity-bucket footprint â€” see
    /// [`DiscoverableSeries`]'s doc comment for what this is used for and
    /// why it is *our* documented contract's answer, not Prometheus's.
    pub fn discoverable_series(&self) -> Vec<DiscoverableSeries> {
        let mut out = Vec::with_capacity(self.expected_series);

        for g in &self.gauges {
            let last_idx = g.stale_from.unwrap_or(self.sample_count).saturating_sub(1);
            out.push(DiscoverableSeries {
                labels: sorted_labels(&[
                    ("__name__", GAUGE_METRIC),
                    ("instance", &instance_label(g.instance_idx)),
                    (RUN_ID_LABEL, &self.run_id),
                    ("service", &service_label(g.service_idx)),
                    ("slot", &slot_label(g.slot_idx)),
                ]),
                buckets: self.bucket_span(0, last_idx),
            });
        }

        for c in &self.counters {
            out.push(DiscoverableSeries {
                labels: sorted_labels(&[
                    ("__name__", COUNTER_METRIC),
                    ("instance", &instance_label(c.instance_idx)),
                    (RUN_ID_LABEL, &self.run_id),
                    ("service", &service_label(c.service_idx)),
                ]),
                buckets: self.bucket_span(0, self.sample_count.saturating_sub(1)),
            });
        }

        for h in &self.histograms {
            let buckets = self.bucket_span(0, self.sample_count.saturating_sub(1));
            let instance = instance_label(h.instance_idx);
            let service = service_label(h.service_idx);
            let bucket_metric = histogram_bucket_metric_name();
            for i in 0..=self.histogram_bounds.len() {
                let le = if i < self.histogram_bounds.len() {
                    render_bound(self.histogram_bounds[i])
                } else {
                    render_bound(f64::INFINITY)
                };
                out.push(DiscoverableSeries {
                    labels: sorted_labels(&[
                        ("__name__", &bucket_metric),
                        ("instance", &instance),
                        ("le", &le),
                        (RUN_ID_LABEL, &self.run_id),
                        ("service", &service),
                    ]),
                    buckets: buckets.clone(),
                });
            }
            out.push(DiscoverableSeries {
                labels: sorted_labels(&[
                    ("__name__", &histogram_sum_metric_name()),
                    ("instance", &instance),
                    (RUN_ID_LABEL, &self.run_id),
                    ("service", &service),
                ]),
                buckets: buckets.clone(),
            });
            out.push(DiscoverableSeries {
                labels: sorted_labels(&[
                    ("__name__", &histogram_count_metric_name()),
                    ("instance", &instance),
                    (RUN_ID_LABEL, &self.run_id),
                    ("service", &service),
                ]),
                buckets,
            });
        }

        out
    }

    /// The set of distinct activity-bucket floors this corpus's real
    /// samples touch over `[first_idx, last_idx]` (inclusive, clamped to
    /// `sample_count`) â€” a series active continuously across a bucket
    /// boundary touches two buckets, one otherwise; [`ACTIVITY_BUCKET_MS`]
    /// (1h) is far larger than this corpus's own span, so in practice this
    /// is one or two entries, never computed as an assumption.
    fn bucket_span(&self, first_idx: usize, last_idx: usize) -> BTreeSet<i64> {
        let last_idx = last_idx.min(self.sample_count.saturating_sub(1));
        (first_idx..=last_idx)
            .map(|idx| activity_bucket(ts_ms(self, idx)))
            .collect()
    }
}

fn sorted_labels(pairs: &[(&str, &str)]) -> Vec<(String, String)> {
    let mut v: Vec<(String, String)> = pairs
        .iter()
        .map(|(k, val)| (k.to_string(), val.to_string()))
        .collect();
    v.sort();
    v
}

/// splitmix64 (public-domain mix; see `xtask/src/bench/dataset.rs`'s
/// identical constant/shape) â€” the sole PRNG primitive this generator
/// uses, always seeded from a deterministic combination of indices, never
/// threaded as mutable stream state (module doc comment).
fn splitmix64(mut x: u64) -> u64 {
    x = x.wrapping_add(0x9E37_79B9_7F4A_7C15);
    let mut z = x;
    z = (z ^ (z >> 30)).wrapping_mul(0xBF58_476D_1CE4_E5B9);
    z = (z ^ (z >> 27)).wrapping_mul(0x94D0_49BB_1331_11EB);
    z ^ (z >> 31)
}

/// Chains [`splitmix64`] over every element of `parts`, combined with
/// `seed` and a family-specific tag (the first element of `parts` by
/// convention below) â€” the deterministic "index tuple -> pseudo-random
/// u64" primitive every `*_value`/`*_bucket_counts` fn below is built on.
fn mix(seed: u64, parts: &[u64]) -> u64 {
    let mut acc = seed;
    for &p in parts {
        acc = splitmix64(acc ^ p);
    }
    acc
}

/// Every 6th gauge series (by flat generation order) goes stale â€” small
/// enough that the majority of the family stays fully populated (so
/// selectors/aggregations still have plenty of live data), large enough
/// that every representative selector in the query matrix
/// (`service="svc-3"`, `service=~"svc-(1|2)"`, ...) is guaranteed to
/// include at least one stale series.
const GAUGE_STALE_MODULUS: u64 = 6;
/// Every 5th counter series resets â€” an independent modulus from
/// [`GAUGE_STALE_MODULUS`] so the two staleness mechanisms don't always
/// land on the same flat index.
const COUNTER_RESET_MODULUS: u64 = 5;

/// Matches Prometheus's `--query.lookback-delta` default and PulsusDB's
/// `pulsus_promql::plan::DEFAULT_LOOKBACK_MS` (both 5 minutes) â€” used
/// only to time the engineered gauge staleness gap (issue #33 code
/// review, [medium]): a gap that never ages past the lookback by the
/// corpus's last evaluated timestamp is never actually excluded by
/// either engine's staleness rule, so "staleness as gaps" would never be
/// differentially exercised â€” both engines would just keep serving the
/// last real sample via lookback the whole time.
const LOOKBACK_MS: i64 = 300_000;

/// PulsusDB's activity-bucket size (`PULSUS_SERIES_ACTIVITY_BUCKET`
/// default, `pulsus_config::model::ReaderConfig::default`, docs/
/// schemas.md Â§2.1) â€” none of `deploy/e2e/compose.{single,cluster}.yaml`
/// override it, so this is exactly what the live stack uses. Used only to
/// compute the harness's own bucket-floor discovery expectation (issue
/// #33 architect adjudication, finding 2) â€” never to change ingestion or
/// query behavior.
pub const ACTIVITY_BUCKET_MS: i64 = 3_600_000;

/// `floor_to_activity_bucket` (`pulsus_model::floor_to_activity_bucket`),
/// duplicated rather than imported for the same "no internal-crate
/// dependency" reason as this module's other small wire-format/model
/// primitives (see `metrics.rs`'s `STALE_NAN_BITS` doc comment for the
/// precedent). Positive-only domain (this scenario's timestamps are
/// always real, recent Unix milliseconds â€” never negative), so plain
/// truncating integer division already matches ClickHouse's `intDiv`
/// (the two diverge only for negative operands).
pub fn activity_bucket(ms: i64) -> i64 {
    (ms / ACTIVITY_BUCKET_MS) * ACTIVITY_BUCKET_MS
}

/// Per-service gauge-value stride (issue #33 architect plan risk 4): large
/// enough that the maximum possible per-instance/slot offset+noise below
/// (bounded by `instances * 1000 + slots * 100 + 99`) can never approach
/// half this stride for any tier this generator is sized for â€” so
/// per-service sums/extrema can never cross between adjacent services.
/// `5e8` is also the literal threshold the pinned `> bool 5e8` query
/// matrix entry compares against (architect plan interfaces) â€” sized so
/// that threshold splits the (1-indexed) service range into a
/// non-degenerate true/false mix, not an all-or-nothing comparison.
const GAUGE_SERVICE_STRIDE: f64 = 2.0e8;
/// Per-service counter increment-rate stride, same rationale as
/// [`GAUGE_SERVICE_STRIDE`] but for the (per-sample) increment magnitude
/// rather than the cumulative value directly â€” the accumulated *sum*
/// across a service's instances inherits the same non-overlap guarantee
/// (see [`tests::per_service_gauge_and_counter_totals_are_strictly_ordered`]).
const COUNTER_SERVICE_STRIDE: f64 = 1000.0;

const GAUGE_TAG: u64 = 0xA1;
const COUNTER_TAG: u64 = 0xB2;
const HISTOGRAM_TAG: u64 = 0xC3;

/// This gauge series' value at `ts_idx` â€” the per-service stride (above)
/// plus a small, strictly-bounded per-instance/slot offset and a
/// deterministic noise term (`0..100`), pure in `(seed, g, ts_idx)`.
pub fn gauge_value(seed: u64, g: &GaugeSeries, ts_idx: usize) -> f64 {
    let base = (g.service_idx as f64 + 1.0) * GAUGE_SERVICE_STRIDE
        + g.instance_idx as f64 * 1000.0
        + g.slot_idx as f64 * 100.0;
    let noise = mix(
        seed,
        &[
            GAUGE_TAG,
            g.service_idx as u64,
            g.instance_idx as u64,
            g.slot_idx as u64,
            ts_idx as u64,
        ],
    ) % 100;
    base + noise as f64
}

/// This gauge series' emission state at `ts_idx`: `true` once `ts_idx`
/// reaches [`GaugeSeries::stale_from`] (never for a series with no
/// staleness engineered).
pub fn gauge_is_stale(g: &GaugeSeries, ts_idx: usize) -> bool {
    g.stale_from.is_some_and(|s| ts_idx >= s)
}

/// This counter series' per-sample increment at `ts_idx` â€” always
/// positive (a real counter only ever increases between resets), pure in
/// `(seed, c, ts_idx)`.
fn counter_increment(seed: u64, c: &CounterSeries, ts_idx: usize) -> f64 {
    let base_rate = (c.service_idx as f64 + 1.0) * COUNTER_SERVICE_STRIDE;
    let noise = mix(
        seed,
        &[
            COUNTER_TAG,
            c.service_idx as u64,
            c.instance_idx as u64,
            ts_idx as u64,
        ],
    ) % 50;
    base_rate + noise as f64
}

/// This counter series' cumulative value at `ts_idx`: the sum of every
/// increment since the last reset at-or-before `ts_idx` (`0.0` exactly at
/// the reset index itself â€” "value drops to 0", architect plan). Re-sums
/// from scratch on every call (module doc comment: no threaded RNG/running
/// -total state) â€” `sample_count` is small (tens), so this stays O(1) in
/// practice.
pub fn counter_value(seed: u64, c: &CounterSeries, ts_idx: usize) -> f64 {
    let start = match c.reset_at {
        Some(r) if ts_idx == r => return 0.0,
        Some(r) if ts_idx > r => r + 1,
        _ => 0,
    };
    (start..=ts_idx)
        .map(|k| counter_increment(seed, c, k))
        .sum()
}

/// This histogram data point's per-bucket-range *increment* at `ts_idx`
/// for bucket `bucket_idx` â€” every increment in `1..=10`, so a bucket's
/// running total (below) is always strictly increasing, pure in `(seed,
/// h, ts_idx, bucket_idx)`.
fn histogram_bucket_increment(
    seed: u64,
    h: &HistogramSeries,
    ts_idx: usize,
    bucket_idx: usize,
) -> u64 {
    1 + mix(
        seed,
        &[
            HISTOGRAM_TAG,
            h.service_idx as u64,
            h.instance_idx as u64,
            ts_idx as u64,
            bucket_idx as u64,
        ],
    ) % 10
}

/// This histogram data point's per-bucket-range counts at `ts_idx`
/// (`bounds_len + 1` entries â€” one per explicit bound, plus the implicit
/// `+Inf` overflow bucket the OTLP wire format always carries last) â€”
/// **cumulative since collection start** (OTLP `AggregationTemporality
/// ::CUMULATIVE` semantics, architect plan: "OTLP explicit-bucket
/// Histogram datapoints"), i.e. each bucket's own value across successive
/// `ts_idx` is monotonically non-decreasing, exactly like [`counter_value`]
/// â€” **not** an independent per-timestamp draw (an earlier version of this
/// fn was: live-run finding, issue #33 â€” feeding `rate()`/
/// `histogram_quantile` a bucket sequence that isn't actually cumulative
/// over time is invalid input under either engine's counter-reset
/// heuristics and produced spurious, engine-dependent divergences on
/// otherwise-correct code). Histograms have no staleness/resets
/// engineered (module doc comment), so this always accumulates from
/// `ts_idx == 0`, unconditionally. `bounds_len + 1` independent per-
/// timestamp increments feed each bucket's running total â€” see
/// [`histogram_bucket_increment`].
pub fn histogram_bucket_counts(
    seed: u64,
    h: &HistogramSeries,
    ts_idx: usize,
    bounds_len: usize,
) -> Vec<u64> {
    (0..=bounds_len)
        .map(|bucket_idx| {
            (0..=ts_idx)
                .map(|t| histogram_bucket_increment(seed, h, t, bucket_idx))
                .sum()
        })
        .collect()
}

/// A plausible (not physically meaningful â€” no invariant depends on it
/// beyond being finite and reproducible) `sum` for a histogram data point:
/// each bucket's count weighted by its bound-interval midpoint (the
/// overflow bucket weighted at `2x` the last finite bound).
fn histogram_sum(bounds: &[f64], counts: &[u64]) -> f64 {
    let mut total = 0.0;
    for (i, &c) in counts.iter().enumerate() {
        let mid = if i == 0 {
            bounds.first().copied().unwrap_or(0.0) / 2.0
        } else if i < bounds.len() {
            (bounds[i - 1] + bounds[i]) / 2.0
        } else {
            bounds.last().copied().unwrap_or(1.0) * 2.0
        };
        total += c as f64 * mid;
    }
    total
}

/// Renders a histogram bound the same way the collector's OTLPâ†’PRW
/// conversion does â€” Go's shortest-round-trip decimal, matching Rust's
/// `f64` `Display` for finite values (same convention `pulsus-write::
/// protocols::otlp_metrics::render_bound` documents; duplicated here for
/// the same "no internal-crate dependency" reason as [`activity_bucket`]).
/// Used only by [`Corpus::discoverable_series`] to compute the expected
/// `le` label for each histogram bucket series.
fn render_bound(value: f64) -> String {
    if value.is_finite() {
        format!("{value}")
    } else {
        "+Inf".to_string()
    }
}

/// The sample index a deterministic subset of gauge series stop emitting
/// at, chosen so that the corpus's **last** evaluated timestamp (index
/// `sample_count - 1` â€” what every [`Mode::Instant`] query in the matrix
/// evaluates at, and the last step every [`Mode::Range`] query covers,
/// mirrors `metrics.rs`) lands strictly more than [`LOOKBACK_MS`] after
/// the gapped series' last real sample (issue #33 code review, [medium]):
/// a full extra step of headroom on top of the lookback boundary itself,
/// since the lookback window is left-open (`pulsus_promql::eval::
/// staleness`: a sample exactly `lookback_ms` old is still excluded, but
/// only just) â€” the margin must survive `step_ms` granularity, not land
/// exactly on the boundary. Falls back to "as early as possible" (index
/// `1`) when the window is too short to reach that margin at all (e.g. a
/// small synthetic corpus in this module's own unit tests) â€” those
/// corpora don't drive a live differential, so there is nothing to
/// preserve a lookback-crossing margin for.
fn stale_cutoff_for(sample_count: usize, step_ms: i64) -> usize {
    let max_idx = sample_count.saturating_sub(1);
    let lookback_samples = if step_ms > 0 {
        (LOOKBACK_MS / step_ms) as usize
    } else {
        0
    };
    max_idx
        .saturating_sub(lookback_samples + 1)
        .max(1)
        .min(max_idx.saturating_sub(1).max(1))
}

/// Builds every gauge/counter/histogram series identity for `spec`
/// (flat, deterministic generation order â€” [`GAUGE_STALE_MODULUS`]/
/// [`COUNTER_RESET_MODULUS`] key off this order), then derives the
/// completeness manifest (`expected_series`/`expected_samples`, issue #33
/// amendment) from them. Pure in `spec` (unit-tested determinism below).
pub fn generate(spec: &CorpusSpec) -> Corpus {
    let f = &spec.families;
    let stale_cutoff = stale_cutoff_for(spec.sample_count, spec.step_ms);
    let reset_at = (spec.sample_count / 2)
        .max(1)
        .min(spec.sample_count.saturating_sub(1).max(1));

    let mut gauges = Vec::with_capacity(f.services * f.gauge_instances * f.gauge_slots);
    let mut flat = 0u64;
    for service_idx in 0..f.services {
        for instance_idx in 0..f.gauge_instances {
            for slot_idx in 0..f.gauge_slots {
                let stale_from = flat
                    .is_multiple_of(GAUGE_STALE_MODULUS)
                    .then_some(stale_cutoff);
                gauges.push(GaugeSeries {
                    service_idx,
                    instance_idx,
                    slot_idx,
                    stale_from,
                });
                flat += 1;
            }
        }
    }

    let mut counters = Vec::with_capacity(f.services * f.counter_instances);
    let mut flat = 0u64;
    for service_idx in 0..f.services {
        for instance_idx in 0..f.counter_instances {
            let reset = flat
                .is_multiple_of(COUNTER_RESET_MODULUS)
                .then_some(reset_at);
            counters.push(CounterSeries {
                service_idx,
                instance_idx,
                reset_at: reset,
            });
            flat += 1;
        }
    }

    let mut histograms = Vec::with_capacity(f.services * f.histogram_instances);
    for service_idx in 0..f.services {
        for instance_idx in 0..f.histogram_instances {
            histograms.push(HistogramSeries {
                service_idx,
                instance_idx,
            });
        }
    }

    let bucket_series_per_histogram = spec.histogram_bounds.len() + 1; // + implicit +Inf
    let series_per_histogram = bucket_series_per_histogram + 2; // + _sum + _count

    let mut series =
        Vec::with_capacity(gauges.len() + counters.len() + histograms.len() * series_per_histogram);
    for g in &gauges {
        series.push(SeriesSpec {
            metric_name: GAUGE_METRIC.to_string(),
            samples_emitted: g.stale_from.unwrap_or(spec.sample_count),
        });
    }
    for _ in &counters {
        series.push(SeriesSpec {
            metric_name: COUNTER_METRIC.to_string(),
            samples_emitted: spec.sample_count,
        });
    }
    for _ in &histograms {
        for _ in 0..bucket_series_per_histogram {
            series.push(SeriesSpec {
                metric_name: histogram_bucket_metric_name(),
                samples_emitted: spec.sample_count,
            });
        }
        series.push(SeriesSpec {
            metric_name: histogram_sum_metric_name(),
            samples_emitted: spec.sample_count,
        });
        series.push(SeriesSpec {
            metric_name: histogram_count_metric_name(),
            samples_emitted: spec.sample_count,
        });
    }

    let expected_series = series.len();
    let expected_samples: usize = series.iter().map(|s| s.samples_emitted).sum();

    let first_ts_ms = spec.base_ms;
    let last_ts_ms = spec.base_ms + spec.step_ms * (spec.sample_count.saturating_sub(1) as i64);

    Corpus {
        run_id: spec.run_id.clone(),
        scale: spec.scale,
        seed: spec.seed,
        base_ms: spec.base_ms,
        step_ms: spec.step_ms,
        sample_count: spec.sample_count,
        gauges,
        counters,
        histograms,
        histogram_bounds: spec.histogram_bounds.clone(),
        series,
        expected_series,
        expected_samples,
        first_ts_ms,
        last_ts_ms,
    }
}

/// This corpus's `ts_idx`'th sample timestamp, milliseconds.
pub fn ts_ms(c: &Corpus, ts_idx: usize) -> i64 {
    c.base_ms + c.step_ms * ts_idx as i64
}

fn otlp_attr(key: &str, value: &str) -> serde_json::Value {
    serde_json::json!({ "key": key, "value": { "stringValue": value } })
}

/// Renders this corpus's `service_idx`'th service label value â€” `"svc-N"`,
/// matching the literal selectors the pinned query matrix uses
/// (`service="svc-3"`, `service=~"svc-(1|2)"`).
pub fn service_label(service_idx: usize) -> String {
    format!("svc-{service_idx}")
}

/// Renders an instance label value, zero-padded to 3 digits (`"inst-007"`)
/// so the pinned `instance!~"inst-0.*"` selector splits the population
/// into a non-degenerate matched/unmatched mix (instances `0..99` match,
/// the rest don't) rather than an all-or-nothing regex.
pub fn instance_label(instance_idx: usize) -> String {
    format!("inst-{instance_idx:03}")
}

pub fn slot_label(slot_idx: usize) -> String {
    format!("slot-{slot_idx}")
}

/// Builds this corpus's OTLP `ExportMetricsServiceRequest` JSON bodies,
/// **one per timestamp, in ascending order** (architect plan: "batched per
/// timestamp (â‰¤ one timestamp-slice per request) ... in ascending
/// timestamp order (remote-write in-order requirement)"). Each request
/// carries at most three `Metric` descriptors (gauge/sum/histogram), each
/// with one data point per series that is live at that timestamp (a gauge
/// past its `stale_from` index contributes none â€” the OTLP-side
/// realization of a staleness gap).
pub fn to_otlp_export_requests(c: &Corpus) -> Vec<serde_json::Value> {
    (0..c.sample_count)
        .map(|ts_idx| build_request(c, ts_idx))
        .collect()
}

fn build_request(c: &Corpus, ts_idx: usize) -> serde_json::Value {
    let time_ns = ((ts_ms(c, ts_idx) as i128) * 1_000_000).to_string();

    let gauge_points: Vec<serde_json::Value> = c
        .gauges
        .iter()
        .filter(|g| !gauge_is_stale(g, ts_idx))
        .map(|g| {
            let attrs = vec![
                otlp_attr("service", &service_label(g.service_idx)),
                otlp_attr("instance", &instance_label(g.instance_idx)),
                otlp_attr("slot", &slot_label(g.slot_idx)),
                otlp_attr(RUN_ID_LABEL, &c.run_id),
            ];
            serde_json::json!({
                "attributes": attrs,
                "timeUnixNano": time_ns,
                "asDouble": gauge_value(c.seed, g, ts_idx),
            })
        })
        .collect();

    let counter_points: Vec<serde_json::Value> = c
        .counters
        .iter()
        .map(|cs| {
            let attrs = vec![
                otlp_attr("service", &service_label(cs.service_idx)),
                otlp_attr("instance", &instance_label(cs.instance_idx)),
                otlp_attr(RUN_ID_LABEL, &c.run_id),
            ];
            serde_json::json!({
                "attributes": attrs,
                "timeUnixNano": time_ns,
                "asDouble": counter_value(c.seed, cs, ts_idx),
            })
        })
        .collect();

    let histogram_points: Vec<serde_json::Value> = c
        .histograms
        .iter()
        .map(|h| {
            let counts = histogram_bucket_counts(c.seed, h, ts_idx, c.histogram_bounds.len());
            let count: u64 = counts.iter().sum();
            let sum = histogram_sum(&c.histogram_bounds, &counts);
            let attrs = vec![
                otlp_attr("service", &service_label(h.service_idx)),
                otlp_attr("instance", &instance_label(h.instance_idx)),
                otlp_attr(RUN_ID_LABEL, &c.run_id),
            ];
            serde_json::json!({
                "attributes": attrs,
                "timeUnixNano": time_ns,
                "count": count.to_string(),
                "sum": sum,
                "bucketCounts": counts.iter().map(u64::to_string).collect::<Vec<_>>(),
                "explicitBounds": c.histogram_bounds,
            })
        })
        .collect();

    let mut metrics = Vec::with_capacity(3);
    if !gauge_points.is_empty() {
        metrics.push(serde_json::json!({
            "name": GAUGE_METRIC,
            "unit": "By",
            "description": "issue #33 e2e differential gauge corpus",
            "gauge": { "dataPoints": gauge_points },
        }));
    }
    metrics.push(serde_json::json!({
        "name": COUNTER_METRIC,
        "unit": "1",
        "description": "issue #33 e2e differential counter corpus",
        "sum": {
            "dataPoints": counter_points,
            "aggregationTemporality": "AGGREGATION_TEMPORALITY_CUMULATIVE",
            "isMonotonic": true,
        },
    }));
    metrics.push(serde_json::json!({
        "name": HISTOGRAM_METRIC,
        "unit": "s",
        "description": "issue #33 e2e differential histogram corpus",
        "histogram": {
            "dataPoints": histogram_points,
            "aggregationTemporality": "AGGREGATION_TEMPORALITY_CUMULATIVE",
        },
    }));

    serde_json::json!({
        "resourceMetrics": [{
            "resource": { "attributes": [] },
            "scopeMetrics": [{ "metrics": metrics }],
        }]
    })
}

