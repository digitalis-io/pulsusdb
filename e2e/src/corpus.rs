/// from `scenarios.rs`, since that constant is scoped to the logs
/// scenario's own module-private conventions.
pub const RUN_ID_LABEL: &str = "run_id";

/// Which tier a [`CorpusSpec`] sizes its families to (issue #33 amendment:
