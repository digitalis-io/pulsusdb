//! Deterministic metric corpus generator for the M2 differential scenario
//! (issue #33 architect plan + amendment): three families (gauges with
//! staleness gaps, monotonic counters with resets, OTLP explicit-bucket
//! histograms) pushed **once** as OTLP metrics into the collector, which
//! fans them out â€” via two identical `prometheusremotewrite` exporters â€”
//! to both PulsusDB and a reference Prometheus (`e2e/src/metrics.rs`).
//! Because both backends ingest the collector's **own** OTLPâ†’PRW
//! conversion (not `pulsus-write`'s Rust OTLP-metrics parser, issue #27),
//! this module only needs to emit spec-valid OTLP â€” it never needs to
//! reproduce the collector's or either backend's histogram-flattening
//! arithmetic.
//!
//! **PRNG:** hand-rolled `splitmix64` (issue #16 task-manager resolution
//! #3 precedent, `xtask/src/bench/dataset.rs`) â€” never `rand`, so a given
//! `(seed, scale)` corpus is byte-reproducible across `rand` version
//! bumps. Duplicated here rather than shared: `e2e/` has no dependency on
//! `xtask`, and `dataset.rs`'s own doc comment already establishes
//! "duplicate, don't share" as this codebase's convention for this exact
//! generator shape.
//!
//! **Determinism contract:** every value is a pure function of `(seed,
//! service_idx, instance_idx, slot_idx, ts_idx)` â€” no accumulated RNG
//! state threaded between calls (`counter_value` re-sums from the last
//! reset on every call rather than mutating a running total), so the
//! whole corpus can be regenerated byte-for-byte from a `CorpusSpec` alone
//! (unit tests below assert this).
//!
//! **Distinct-topk invariant (architect plan risk 4):** every family's
//! per-service base value is offset by a stride far larger than the
//! maximum possible per-instance noise summed across every instance in a
//! service, so `sum by(service)(...)` aggregates â€” and therefore
//! `topk`/`bottomk`/comparison-operator results â€” can never tie or cross
//! between services, at any timestamp, regardless of how staleness gaps
//! or counter resets fall. Verified by
//! [`tests::per_service_gauge_and_counter_totals_are_strictly_ordered`].

use std::collections::{BTreeMap, BTreeSet};

use serde::{Deserialize, Serialize};

/// Which family every identifying label is emitted under (issue #33
/// architect plan: "All identifying labels are emitted as OTLP datapoint
/// attributes, not resource attributes" â€” `add_metric_suffixes: false` is
/// pinned on both collector exporters, so these names travel verbatim to
/// both backends).
pub const GAUGE_METRIC: &str = "mem_usage_bytes";
pub const COUNTER_METRIC: &str = "requests_total";
pub const HISTOGRAM_METRIC: &str = "request_duration_seconds";

/// The histogram family's collector-flattened suffix names (module doc
/// comment: the actual `_bucket`/`_sum`/`_count` split happens in the
/// collector's OTLPâ†’PRW conversion, not in this crate â€” these are what
/// that conversion is documented to produce). Functions, not consts
/// (`format!` isn't `const`-evaluable) â€” used both by [`generate`]'s
/// [`SeriesSpec`] bookkeeping and by `metrics.rs`'s completeness
/// pre-check ([`Corpus::per_metric_manifest`]), which must query
/// PulsusDB's `/api/v1/query` per **concrete** metric name: a bare
/// label-only selector with no `__name__` is `422 Unprocessable Entity`
/// ("not yet supported: selector without a concrete metric name" â€”
/// docs/schemas.md's metric-scoped model requires one), unlike real
/// Prometheus.
pub fn histogram_bucket_metric_name() -> String {
    format!("{HISTOGRAM_METRIC}_bucket")
}
pub fn histogram_sum_metric_name() -> String {
    format!("{HISTOGRAM_METRIC}_sum")
}
pub fn histogram_count_metric_name() -> String {
    format!("{HISTOGRAM_METRIC}_count")
}

/// The label every series in this scenario's corpus (and its StaleNaN
/// micro-case, `metrics.rs`) carries, scoping every selector in the query
