/// Mechanical consistency test (issue #34 CODE review round-2, item 4).
///
/// **Honest scope (round-3 [low] finding #4):** this asserts the report's
/// comparative *claims* against the committed **JSON** evidence
/// (`docs/benchmarks/data/metrics-labels-ci.json`) — it is not a literal
/// diff against `docs/benchmarks/m2-metrics-label-resolution.md`'s prose or
/// tables, and cannot catch a hand-transcription typo in the markdown that
/// still happens to agree with these assertions' *conclusions* (e.g. "idx
/// wins" stated correctly but with a wrong millisecond figure quoted next
/// to it). What it *does* catch, which is the failure mode that actually
/// occurred across this issue's review rounds: a future regeneration that
/// changes the underlying numbers enough to flip a stated conclusion (a
/// win/loss split, a ratio's saturation point, a materiality threshold)
/// without the prose being updated to match. Reads a real file (not a live
/// database), so this runs in the ordinary `cargo test -p xtask` pass, no
/// `PULSUS_TEST_CLICKHOUSE` gate needed.
#[cfg(test)]
mod consistency_tests {
    use super::*;

    fn load_committed_report() -> MetricsLabelsReport {
        // `CARGO_MANIFEST_DIR` for this crate is `<repo>/xtask`; the
        // committed artifact lives at `<repo>/docs/benchmarks/data/...`.
        let path = concat!(
            env!("CARGO_MANIFEST_DIR"),
            "/../docs/benchmarks/data/metrics-labels-ci.json"
        );
        let json = std::fs::read_to_string(path).unwrap_or_else(|e| {
            panic!(
                "failed to read the committed evidence artifact at {path}: {e} — this test \
                 asserts the m2-metrics-label-resolution.md report's comparative claims against \
                 it; regenerate via `cargo xtask bench metrics-labels --profile ci` if it is \
                 genuinely missing"
            )
        });
        serde_json::from_str(&json).unwrap_or_else(|e| {
            panic!("failed to deserialize the committed evidence artifact at {path}: {e}")
        })
    }

    fn find<'a>(
        report: &'a MetricsLabelsReport,
        bucket_ms: i64,
        path: &str,
        cardinality: u64,
        selector_substr: &str,
    ) -> &'a PathEvidence {
        report
            .buckets
            .iter()
            .find(|b| b.bucket_ms == bucket_ms)
            .unwrap_or_else(|| panic!("no bucket_ms={bucket_ms} in the committed artifact"))
            .paths
            .iter()
            .find(|p| {
                p.path == path
                    && p.cardinality == cardinality
                    && p.selector.contains(selector_substr)
            })
            .unwrap_or_else(|| {
                panic!(
                    "no cell path={path} cardinality={cardinality} selector~={selector_substr:?} \
                     in the committed artifact"
                )
            })
    }

    /// The report's scale-invariant comparative claim: **idx loses to the
    /// SQL fallback on the wide scans (`Regex5xx`, `NegBroad`) at every
    /// committed cardinality** — issue #34 CODE review round-2 [valid]
    /// finding #1 (the prose previously asserted "idx > sql in every
    /// row", which the committed JSON itself refutes), refined by round-3
    /// [precision] finding #2 into two separate claims: this test covers
    /// the scale-invariant wide-scan loss (checked at all three committed
    /// cardinalities, both bucket sizes); the bounded positive-equality
    /// selectors' cardinality-dependent *crossover* is the separate
    /// [`idx_vs_sql_fallback_on_bounded_positive_selectors_crosses_over_by_cardinality`]
    /// test below.
    #[test]
    fn idx_loses_to_sql_fallback_on_wide_scans_at_every_committed_cardinality() {
        let report = load_committed_report();
        for &bucket_ms in &[3_600_000i64, 86_400_000i64] {
            for &card in &[1_000u64, 10_000, 50_000] {
                let sql_regex = find(&report, bucket_ms, "sql_fallback", card, "status=~");
                let idx_regex = find(&report, bucket_ms, "idx_prototype", card, "status=~");
                assert!(
                    idx_regex.wall_ms_p50 > sql_regex.wall_ms_p50,
                    "bucket_ms={bucket_ms} cardinality={card}: expected idx to lose to \
                     sql_fallback on Regex5xx (idx={} sql={})",
                    idx_regex.wall_ms_p50,
                    sql_regex.wall_ms_p50
                );

                let sql_neg = find(&report, bucket_ms, "sql_fallback", card, "job!=");
                let idx_neg = find(&report, bucket_ms, "idx_prototype", card, "job!=");
                assert!(
                    idx_neg.wall_ms_p50 > sql_neg.wall_ms_p50,
                    "bucket_ms={bucket_ms} cardinality={card}: expected idx to lose to \
                     sql_fallback on NegBroad (idx={} sql={})",
                    idx_neg.wall_ms_p50,
                    sql_neg.wall_ms_p50
                );
            }
        }
    }

    /// The idx-vs-sql-fallback win/loss split on the two **bounded
    /// positive-equality** selectors is **not** scale-invariant (issue #34
    /// CODE review round-3 [precision] finding #2 — round-2's version of
    /// this test asserted only `metric_50000` and so overclaimed a
    /// uniform "idx wins" verdict). It is a **crossover**: sql_fallback
    /// wins both `NarrowEq` and `BroadEq` at the smallest committed
    /// cardinality (1,000), idx wins both at the largest (50,000), and the
    /// crossover itself sits somewhere around 10,000 (`BroadEq` has
    /// already crossed by 10,000 in both bucket passes; `NarrowEq`'s
    /// crossover point falls close enough to 10,000 that this corpus's
    /// two bucket passes disagree on which side of it 10,000 lands —
    /// **deliberately not asserted** here to avoid a flaky assertion on
    /// bench-run noise; the two endpoints below are the stable,
    /// unambiguous claim).
    #[test]
    fn idx_vs_sql_fallback_on_bounded_positive_selectors_crosses_over_by_cardinality() {
        let report = load_committed_report();
        for &bucket_ms in &[3_600_000i64, 86_400_000i64] {
            // Low cardinality (1,000): sql_fallback wins everywhere,
            // including the bounded positive-equality selectors.
            for sel in ["pod=", "job=\"j0\""] {
                let sql = find(&report, bucket_ms, "sql_fallback", 1_000, sel);
                let idx = find(&report, bucket_ms, "idx_prototype", 1_000, sel);
                assert!(
                    idx.wall_ms_p50 > sql.wall_ms_p50,
                    "bucket_ms={bucket_ms} cardinality=1000 selector={sel:?}: expected \
                     sql_fallback to win at low cardinality (idx={} sql={})",
                    idx.wall_ms_p50,
                    sql.wall_ms_p50
                );
            }

            // High cardinality (50,000): idx has overtaken sql_fallback on
            // both bounded positive-equality selectors — the crossover
            // has completed.
            for sel in ["pod=", "job=\"j0\""] {
                let sql = find(&report, bucket_ms, "sql_fallback", 50_000, sel);
                let idx = find(&report, bucket_ms, "idx_prototype", 50_000, sel);
                assert!(
                    idx.wall_ms_p50 < sql.wall_ms_p50,
                    "bucket_ms={bucket_ms} cardinality=50000 selector={sel:?}: expected idx \
                     to have overtaken sql_fallback at high cardinality (idx={} sql={})",
                    idx.wall_ms_p50,
                    sql.wall_ms_p50
                );
            }

            // Mid cardinality (10,000): `BroadEq` has already crossed over
            // (stable in both bucket passes); `NarrowEq` has not (or has
            // only just, depending on bucket size) — asserted loosely
            // (idx must at least be competitive, not asserted as an
            // outright win) to avoid pinning a noise-prone boundary.
            let sql_broad = find(&report, bucket_ms, "sql_fallback", 10_000, "job=\"j0\"");
            let idx_broad = find(&report, bucket_ms, "idx_prototype", 10_000, "job=\"j0\"");
            assert!(
                idx_broad.wall_ms_p50 < sql_broad.wall_ms_p50,
                "bucket_ms={bucket_ms} cardinality=10000: expected idx to have already \
                 crossed over on BroadEq (idx={} sql={})",
                idx_broad.wall_ms_p50,
                sql_broad.wall_ms_p50
            );
        }
    }

    /// The report's structural claim: `read_rows` is identical (metric
    /// scan is already saturated) regardless of bucket size, for the SQL
    /// fallback path.
    #[test]
    fn read_rows_ratio_is_saturated_at_one_for_every_committed_cardinality() {
        let report = load_committed_report();
        for o in &report.over_inclusion {
            assert_eq!(
                o.ratio, 1.0,
                "cardinality={}: expected the structural read_rows ratio to be exactly 1.0 \
                 (metric_series' fingerprint-before-time primary key prunes zero granules \
                 regardless of bucket size) — got {}",
                o.cardinality, o.ratio
            );
        }
    }

    /// The report's semantic over-inclusion claim: the deduplicated
    /// candidate ratio is materially larger than 1 — the observable
    /// `read_rows` cannot show (previous finding), but `matched_candidates`
    /// can.
    #[test]
    fn candidate_ratio_shows_material_over_inclusion() {
        let report = load_committed_report();
        assert!(
            !report.over_inclusion.is_empty(),
            "expected at least one over_inclusion row in the committed artifact"
        );
        for o in &report.over_inclusion {
            assert!(
                o.candidate_ratio > 5.0,
                "cardinality={}: expected a material (>5x) semantic over-inclusion ratio, got {}",
                o.cardinality,
                o.candidate_ratio
            );
        }
    }
}
